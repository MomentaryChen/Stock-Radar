export function translate(lang: Language, key: string): string {
  const dict = lang === "en" ? EN : ZH_TW;
  return dict[key] ?? key;
}

export function useT() {
  const lang = useStore((s) => s.language);
  return (key: string) => translate(lang, key);
}

